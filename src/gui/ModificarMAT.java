    private void BTNBUSCActionPerformed(java.awt.event.ActionEvent evt) {                                        
        String criterio = JOptionPane.showInputDialog(this, "Ingrese el TÃ­tulo o ID del libro a buscar:");
        if (criterio != null && !criterio.trim().isEmpty()) {
            DefaultTableModel modeloTabla = (DefaultTableModel) TablaLibros.getModel();
            modeloTabla.setRowCount(0); // Limpiar filas
            
            List<Libro> listaLibros = Logica.traerLibros();
            if (listaLibros != null) {
                for (Libro libro : listaLibros) {
                    if (String.valueOf(libro.getId()).equals(criterio.trim()) || 
                        libro.getTitulo().toLowerCase().contains(criterio.trim().toLowerCase())) {
                        Object[] objeto = {
                            libro.getId(),
                            libro.getTitulo(),
                            libro.getAutor(),
                            libro.getEditorial(),
                            libro.getISBN(),
                            libro.getNPaginas(),
                            libro.getUnidadesDisponibles()
                        };
                        modeloTabla.addRow(objeto);
                    }
                }
            }
            if (modeloTabla.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "No se encontraron libros con el criterio: " + criterio);
                cargarTabla(); // Recargar completa si no hay resultados
            }
        } else if (criterio != null) { // Si no ingresÃ³ nada pero no cancelÃ³
            cargarTabla(); // Recargar completa
        }
    }                                       


