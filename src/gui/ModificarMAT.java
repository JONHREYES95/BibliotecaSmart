    private void BtnBorrarActionPerformed(java.awt.event.ActionEvent evt) {                                          
        if (TablaLibros.getRowCount() > 0) {
            if (TablaLibros.getSelectedRow() != -1) {
                Long idLibro = (Long) TablaLibros.getValueAt(TablaLibros.getSelectedRow(), 0);
                String tituloLibro = (String) TablaLibros.getValueAt(TablaLibros.getSelectedRow(), 1);
                
                int respuesta = JOptionPane.showConfirmDialog(
                    this, 
                    "Â¿EstÃ¡ seguro de que desea borrar el libro:\n" +
                    "ID: " + idLibro + "\n" +
                    "TÃ­tulo: " + tituloLibro + "?", 
                    "Confirmar Borrado", 
                    JOptionPane.YES_NO_OPTION
                );
                
                if (respuesta == JOptionPane.YES_OPTION) {
                    Logica.eliminarLibro(idLibro);
                    JOptionPane.showMessageDialog(this, "Libro borrado exitosamente.");
                    cargarTabla();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Por favor, seleccione un libro para borrar.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "No hay libros para borrar en la tabla.");
        }
    }                                         

